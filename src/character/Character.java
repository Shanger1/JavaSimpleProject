package character;

public abstract class Character {
	public String race;
	public String profession = "";
	public String racial;
	public String skill = "";
	
	public String getRace() {
		return race;
	}
	public String getProfession() {
		return profession;
	}
	public String getRacial() {
		return racial;
	}
	public String getSkill() {
		return skill;
	
	}
}
